
public class ListNode {

    
    int data;
    ListNode next;
    
    public ListNode() {
        this.data=-1;
        this.next=null;
    }

    public ListNode(int data, ListNode next) {
        this.data = data;
        this.next = next;
    }
    
     public  ListNode(int data )
    {
        this.data=data;
        this.next=null;
    }

}
